#include <iostream>
using namespace std;

int main() {
    // Declaração de variável para armazenar a entrada do usuário
    int numero;

    // Solicita ao usuário para inserir um número
    cout << "Digite um número: ";
    
    // Lê o número inserido pelo usuário
    cin >> numero;

    // Exibe o número de volta na tela
    cout << "Você digitou: " << numero << endl;

    return 0;
}
